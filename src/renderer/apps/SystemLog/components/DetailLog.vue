<template>
  <Modal
    class="detail-modal"
    v-model="isShowModal"
    title="Details"
    :mask-closable="true"
    :closable="true"
    :width="900"
    @on-cancel="handleCancel"
  >
    <Form
      class="detail-form"
      :model="detailInfo"
      :label-width="180"
      v-if="isShowModal"
    >
      <FormItem
        v-for="key in Object.keys(detailInfo).filter(k => !['_index', '_rowKey'].includes(k)).sort()"
        :label="key"
        :prop="key"
        :key="key"
        class="detail-form-item"
      >
        <HighLight :ref="key" lang="Javascript" :data="detailInfo[key]"></HighLight>
      </FormItem>
    </Form>
    <div slot="footer">
      <Button type="primary" @click="handleCancel">shut down</Button>
    </div>
  </Modal>
</template>

<script>
  export default {
    name: 'DetailLog',
    data () {
      return {
        // 是否显示弹窗
        isShowModal: false,
        // Details
        detailInfo: {}
      }
    },
    methods: {
      init: function (detailInfo) {
        let _t = this
        _t.detailInfo = detailInfo
        setTimeout(function () {
          Object.keys(_t.$refs).map(key => {
            if (_t.$refs[key] && _t.$refs[key][0].render) {
              _t.$refs[key][0].render()
            }
          })
        }, 0)
      },
      // shut down弹窗
      handleCancel: function () {
        let _t = this
        // 清空备份数据
        _t.detailInfo = {}
        // shut down弹窗
        _t.isShowModal = false
      }
    },
    mounted: async function () {
      let _t = this
      // 监听
      _t.$utils.bus.$on('Apps/SystemLog/detail', function (data) {
        // 初始化
        _t.init(data.info)
        // 显示弹窗
        _t.isShowModal = true
      })
    },
    beforeDestroy: function () {
      let _t = this
      // 销毁监听
      _t.$utils.bus.$off('Apps/SystemLog/detail')
    }
  }
</script>
